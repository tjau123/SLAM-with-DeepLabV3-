Viewer::Viewer():
    mbFinishRequested(false), mbStopped(false), mbStopRequested(false)
{
    cout << "####in Viewer constructor" << endl;
    mbFinished = true;
}

void Viewer::Run()
{
    cout << "####in Viewer run" << endl;
    mbFinished = false;

    while(1)
    {
        usleep(5000);
        if(Stop())
        {
            while(isStopped())
            {
                usleep(3000);
            }
        }
        if(CheckFinish())
        break;
    }
    SetFinish();
}

void Viewer::RequestFinish()
{
    unique_lock<std::mutex> lock(mMutexFinish);
    mbFinishRequested = true;
}

bool Viewer::CheckFinish()
{
    unique_lock<std::mutex> lock(mMutexFinish);
    return mbFinishRequested;
}

void Viewer::SetFinish()
{
    unique_lock<std::mutex> lock(mMutexFinish);
    mbFinished = true;
}

bool Viewer::isFinished()
{
    unique_lock<std::mutex> lock(mMutexFinish);
    return mbFinished;
}

void Viewer::RequestStop()
{
    unique_lock<std::mutex> lock(mMutexStop);
    if(!mbStopped)
        mbStopRequested = true;
}

bool Viewer::isStopped()
{
    unique_lock<std::mutex> lock(mMutexStop);
    return mbStopped;
}

bool Viewer::Stop()
{
    unique_lock<std::mutex> lock(mMutexStop);
    unique_lock<std::mutex> lock2(mMutexFinish);

    if(mbFinishRequested)
        return false;
    else if(mbStopRequested)
    {
        mbStopped = true;
        mbStopRequested = false;
        return true;
    }

    return false;

}

void Viewer::Release()
{
    unique_lock<std::mutex> lock(mMutexStop);
    mbStopped = false;
}

